## upper() and lower():

text = "Hello niranjan!"

print(text.upper())
print(text.lower())

# HELLO NIRANJAN!
# hello niranjan!

# capitalize():

text = "Hello niranjan!"

print(text.capitalize())

# Hello niranjan!

## title()

text = "Hello niranjan!"
print(text.title())

## Hello Niranjan!

text = " Hello-Niranajn "
print(text.strip())
# Hello-Niranajn

## replace()
text = "Hello niranjan!"
print(text.replace("niranjan", "nia"))

# Hello nia!

# startswith() endswith()
text = "Hello niranjan!"

print(text.startswith("i"))
print(text.endswith("niranjan!"))

# False
# True

